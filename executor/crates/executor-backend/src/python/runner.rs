use std::path::Path;

use aithericon_executor_domain::ExecutorError;

/// Write the Python runner template that wraps user code.
///
/// The runner provides:
/// 1. `inputs` dict — loaded from staged files in `AITHERICON_INPUTS_DIR`
/// 2. `set_output(name, value)` — file-based output that writes JSON to `AITHERICON_OUTPUTS_DIR`
/// 3. If the `aithericon` SDK is installed, upgrades to IPC-backed functions for outputs,
///    artifacts, progress, logging, and metrics.
/// 4. Executes the user code file with all helpers available in scope.
pub async fn write_runner(
    runner_path: &Path,
    user_code_path: &Path,
) -> Result<(), ExecutorError> {
    let user_code_escaped = user_code_path
        .to_string_lossy()
        .replace('\\', "\\\\")
        .replace('\'', "\\'");

    let template = format!(
        r#"#!/usr/bin/env python3
# Generated by PythonBackend — do not edit
import sys
import os
import json

# Setup paths
_run_dir = os.environ.get("AITHERICON_RUN_DIR", os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, _run_dir)
_inputs_dir = os.environ.get("AITHERICON_INPUTS_DIR")
if _inputs_dir:
    sys.path.insert(0, _inputs_dir)
os.chdir(_run_dir)

# --- Load inputs ---
def _load_inputs():
    inputs_dir = os.environ.get("AITHERICON_INPUTS_DIR")
    if not inputs_dir or not os.path.isdir(inputs_dir):
        return {{}}
    result = {{}}
    for entry in os.listdir(inputs_dir):
        path = os.path.join(inputs_dir, entry)
        if not os.path.isfile(path):
            continue
        try:
            with open(path, encoding="utf-8") as f:
                content = f.read()
        except (UnicodeDecodeError, OSError):
            # Binary reference file (e.g. *.npy). Leave it on disk under
            # AITHERICON_INPUTS_DIR; user code can open it by path.
            continue
        try:
            result[entry] = json.loads(content)
        except (json.JSONDecodeError, ValueError):
            result[entry] = content
    return result

inputs = _load_inputs()

# --- Output helper (file-based fallback) ---
def set_output(name, value):
    """Set a named output value. Writes JSON to the outputs directory."""
    outputs_dir = os.environ.get("AITHERICON_OUTPUTS_DIR")
    if outputs_dir:
        os.makedirs(outputs_dir, exist_ok=True)
        with open(os.path.join(outputs_dir, name + ".json"), "w") as f:
            json.dump(value, f)

# --- Try to upgrade to SDK (IPC-backed) ---
try:
    import aithericon
    _ipc_sock = os.environ.get("AITHERICON_IPC_SOCKET", "")
    import sys as _sys
    print(f"[runner] SDK imported, socket={{_ipc_sock}}, python={{_sys.executable}}", file=_sys.stderr)
    aithericon.init()
    print(f"[runner] SDK init ok, connected={{aithericon._client.is_connected()}}", file=_sys.stderr)
    inputs = aithericon.load_inputs() or inputs
    set_output = aithericon.set_output
    log_artifact = aithericon.log_artifact
    update_progress = aithericon.update_progress
    define_phases = aithericon.define_phases
    update_phase = aithericon.update_phase
    log_info = aithericon.log_info
    log_warn = aithericon.log_warn
    log_error = aithericon.log_error
    log_debug = aithericon.log_debug
    log_metric = aithericon.log_metric
except ImportError as _e:
    import sys as _sys
    print(f"[runner] SDK import failed: {{_e}}", file=_sys.stderr)
    pass  # SDK not installed — file-based set_output still works

# --- Execute user code ---
exec(open('{user_code_escaped}', encoding='utf-8').read())

# --- Cleanup ---
try:
    aithericon.shutdown()
except NameError:
    pass
"#
    );

    tokio::fs::write(runner_path, template)
        .await
        .map_err(|e| ExecutorError::StagingFailed(format!("failed to write runner template: {e}")))
}
